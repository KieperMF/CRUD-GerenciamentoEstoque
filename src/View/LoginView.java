    public void Login() {
        try {
            UsuarioDAO dao = new UsuarioDAO();
            String nome = txtUsuarioLogin.getText();
            String senha = txtPassword.getText();
            UsuarioModel usuario = new UsuarioModel(nome, senha);
            ResultSet rsetAdm = dao.AutenticarLoginAdmin(usuario);
            ResultSet rsetVend = dao.AutenticarLoginVendedor(usuario);

            if (rsetAdm.next()) {
                MenuAdminView menu = new MenuAdminView();
                JOptionPane.showMessageDialog(null, "Bem-Vindo Administrador");
                menu.setVisible(true);                
                dispose();
            } else if (rsetVend.next()) {
                MenuVendedorView menu = new MenuVendedorView();
                JOptionPane.showMessageDialog(null, "Bem-Vindo Vendedor");
                menu.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Senha ou Usuario Invalidos");
                txtUsuarioLogin.setText("");
                txtPassword.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro Login View:" + e);
        }
    }
